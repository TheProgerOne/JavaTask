/**
 * 1.2.30 Uniform random numbers.
 * Write a program that prints five uniform random numbers between 0 and 1, their average value,
 * and their minimum and maximum values. Use Math.random(), Math.min(), and Math.max().
 */
import  java.lang.Math;
class Random {

    public static void main(String[] args) {
       double a,b,c,d,f;
       a= Math.random();
       b=Math.random();
       c=Math.random();
       d=Math.random();
       f=Math.random();
       double one= Math.max(a,b);
       double two= Math.max(c,d);
       double whichOneBigger=Math.max(one,two);
       double theLargest=Math.max(whichOneBigger,f);
       System.out.println("1: "+a + " 2: "+b +"3: "+c + " 5: "+d + " 6: "+f);
       System.out.println("The largest number of five : "+theLargest);
       double one1= Math.min(a,b);
       double two2= Math.min(c,d);
       double whichOneSmaller=Math.min(one1,two2);
       double theSmallest=Math.min(whichOneSmaller,f);
       System.out.println("The smallest number of five : "+theSmallest);
    }
}
